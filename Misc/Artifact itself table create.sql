CREATE TABLE `Artifact itself` (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    `Set` VARCHAR(255),
    `Type` VARCHAR(50),
    `Main Stat` VARCHAR(50),
    `Number of Substat` INT,
    `%ATK` BOOLEAN,
    `%HP` BOOLEAN,
    `%DEF` BOOLEAN,
    `ATK` BOOLEAN,
    `HP` BOOLEAN,
    `DEF` BOOLEAN,
    `ER` BOOLEAN,
    `EM` BOOLEAN,
    `Crit Rate` BOOLEAN,
    `Crit DMG` BOOLEAN,
    `Where got it` VARCHAR(100),
    Score VARCHAR(50)
);
